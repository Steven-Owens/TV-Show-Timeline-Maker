    public ShowEditor(Show inShow) {
        this.initComponents();
        this.mShow = inShow;
        this.characterModel = new CollectionListModel<>(this.mShow.getCharacters());
        this.listCharacter.setModel(this.characterModel);
        if (!this.characterModel.isEmpty()) {
            this.characterModel.setSelectedItem(this.characterModel.get(0));
            this.listCharacter.setSelectedIndex(0);
        }
        this.episodeModel = new CollectionListModel<>(this.mShow.getEpisodes());
        this.listEpisode.setModel(this.episodeModel);
        if (!this.episodeModel.isEmpty()) {
            this.episodeModel.setSelectedItem(this.episodeModel.get(0));
            this.listEpisode.setSelectedIndex(0);
        }
        this.validate();
    }

