        final javax.swing.JFrame tempThis = this;
        newEpisodeEditor.runOnClose(() -> {
            mShow.addEpisode(newEpisodeEditor.mEpisode);
            episodeModel.add(newEpisodeEditor.mEpisode);
            tempThis.setVisible(true);
        });
